namespace Skyline\Launchd\Task\Runner;


interface RunnerInterface
{
    /**
     * Gets the function that needs to be called
     *
     * @param int $argc
     * @param array $argv
     * @return bool             Returns true on success or false otherwise
     */
    public function execute($argc, $argv): bool;

    /**
     * Returns true, if the task runner is enabled, and ready to be performed.
     *
     * @return bool
     */
    public function isEnabled(): bool;
}